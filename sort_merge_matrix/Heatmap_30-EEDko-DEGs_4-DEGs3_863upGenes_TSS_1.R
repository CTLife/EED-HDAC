  N1 <- nrow(matrix1)
  matrix2 <- matrix1A
  for (i  in c(1:N1) ) {
      means1A <- mean( as.numeric( matrix1A[i,] ) )
      means1B <- mean( as.numeric( matrix1B[i,] ) )
      if(means1A > means1B+0.01) { matrix2[i,] <- means1B*matrix1A[i,]/means1A}
  }
  return(matrix2) 
}

